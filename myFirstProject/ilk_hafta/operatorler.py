####################### OPERATÖRLER #######################

x = 10
y = 2
z = 3

# Toplama
print(x + y)

# Çıkartma
print(x - y)

# Çarpma
print(x * y)

# Bölme
print(x / y)

# Mod
print(x % y)
print(10 % 3.0)

# Üs Alma (Expansions)
print(x ** y)

# Tam Bölme
print(x // y)
print(x // z)

# Atama Opertaörleri (Python Assignment Operators)

# Eşittir (=)
x = x + 1
x += 1
